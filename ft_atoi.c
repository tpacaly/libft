#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	jh;
	int	nombre;

	i = 0;
	jh = 0;
	nombre = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
			|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == 45)
		jh = 1;
	if ((str[i] == 45) || (str[i] == 43))
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nombre *= 10;
		nombre += ((int)str[i] - 48);
		i++;
	}
	if (jh == 1)
		return (-nombre);
	else
		return (nombre);
}
