#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		s;

	i = 0;
	s = 0;
	while (s1[i])
		i++;
	while (s2[s] && n > 0)
	{
		s1[i] = s2[s];
		i++;
		s++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
